'''
range =>    gera uma sequencia de n numeros
'''

# gera a lista de 1 a 3(0, 1 e 2 - 3 elementos), nao chega ao numero 3
for i in range(3):
    print(i)

#gera a lista de 7 a 10 (7, 8 e 9)
for i in range(7,10):
    print(i)

#gera a lista de 1 a 10 com incremento de 2 (1, 3, 5, 7, e 9)
for i in range(1,10,2):
    print(i)    