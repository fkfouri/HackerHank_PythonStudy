import math

#menor inteiro maior
print(math.ceil(3.14))

#valor absoluto de um numero. Valor em modulo
print(math.fabs(-4))

#maior inteiro menor
print(math.floor(3.14))

#constante de Euler e elevado a 2
print(math.exp(2))

#logaritmo na base 10
print(math.log10(100))

#potencia n por y
print(math.pow(2,3))    

#raiz quadrada
print(math.sqrt(144))

#cosseno/seno radiano
print(math.cos(0))
print(math.sin(30))

#valor de pi
print(math.pi)

#valor de constante euler
print(math.e)